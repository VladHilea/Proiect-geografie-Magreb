				<h2>	Pe lângă limba arabă literară care este limba oficială în toate statele maghrebiene, în mod uzual, sunt folosite diverse dialecte arabe care sunt, în bună parte, reciproc inteligibile. Toate aceste dialecte au fost puternic influențate de dialectele berbere, iar mai târziu de limba franceză și, în unele zone, de spaniolă (nordul Marocului) și de italiană (Libia). Alături de dialectele arabe maghrebiene - ce formează un grup distinct în cadrul dialectelor arabe moderne - în foarte multe regiuni ale Maghreb-lui se vorbesc dialecte berbere. Limba malteză, limba oficială a Maltei, a fost inițial un dialect maghrebian, care a fost, însă, dezvoltat până la a fi o limbă standard modernă.

În anul 2002[1], în Algeria, limba berberă (tamazight) a fost declarată limbă națională alături de limba arabă, care, însă, rămâne singura limbă oficială a statului[2].</h2>
